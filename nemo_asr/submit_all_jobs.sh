#!/bin/bash
# Runs all NeMo submit_jobs_*.sh scripts sequentially.
# Usage: HF_TOKEN=hf_... bash submit_all_jobs.sh

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

scripts=(
    "submit_jobs_parakeet.sh"
    "submit_jobs_canary.sh"
    "submit_jobs_salm.sh"
)

for script in "${scripts[@]}"; do
    echo ""
    echo "▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶"
    echo "  Running: ${script}"
    echo "▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶"
    bash "${SCRIPT_DIR}/${script}" &
done

wait

echo ""
echo "▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶▶"
